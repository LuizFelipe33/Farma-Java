    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Produto produto  = new Produto ();
       
       produto.setProduto(jTextProduto.getText());
       produto.setFormula(jTextFormula.getText());
       produto.setPreco(jFormattedPreco.getText());
       produto.setQuantidade(jTextQuantidade.getText());
       
        JOptionPane.showMessageDialog(null, produto.Salvar());
        
        jTextProduto.setText("");
        jTextFormula.setText("");
        jFormattedPreco.setText("");
        jTextQuantidade.setText("");


    }//GEN-LAST:event_jButton3ActionPerformed

