    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String filePath = "C:\\Users\\pc\\Documents\\NetBeansProjects\\PjFarmacia\\Entregas.txt";
        File file = new File(filePath);
        
    try {
        BufferedReader br = new BufferedReader (new FileReader(file));
        String firstLine = br.readLine().trim();
        String[] ColumnsName = firstLine.split(",");
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setColumnIdentifiers(ColumnsName);
        
        Object[] tableLines = br.lines().toArray();
        
        for(int i = 0; i < tableLines.length; i++)
        {
            String line = tableLines[i].toString().trim();
            String[] dataRow = line.split("_");
            model.addRow(dataRow);
           
                    
        } 
        
    } catch (Exception ex) {
        Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String pesquisarNome = JOptionPane.showInputDialog("Digite um nome para pesquisar");
        if (pesquisarNome.length()> 0){
            for(int i=0;i<jTable1.getRowCount(); i++ ){
                if(pesquisarNome.equals(jTable1.getValueAt(i, 0))){
                    jTextField1.setText(jTable1.getValueAt(i, 0).toString());
                    jTextField3.setText(jTable1.getValueAt(i, 1).toString());
                    jTextField4.setText(jTable1.getValueAt(i,2).toString());
                    jTextField2.setText(jTable1.getValueAt(i,3).toString());
                }
            }
           
            
        } 
    }//GEN-LAST:event_jButton3ActionPerformed

